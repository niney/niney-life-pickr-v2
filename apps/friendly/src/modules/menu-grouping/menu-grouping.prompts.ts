// Ollama structured output — additionalProperties 의 string 값을 강제.
// 키 자체는 입력 표기이므로 schema 로 못 잡지만, 값은 string 으로 묶어둠.
export const MENU_GROUPING_JSON_SCHEMA = {
  type: 'object',
  additionalProperties: { type: 'string' },
} as const;

// 한 호출에 들어가는 입력 메뉴 수의 상한. 이 이상이면 청크로 분할.
// Ollama num_ctx 4096 안에서 입력+출력 합 — 한 메뉴를 평균 30 토큰 잡으면
// 80 개 ≈ 2400 토큰. 시스템 프롬프트 + 출력 여유까지 포함해서 안전한 한도.
export const MENU_GROUPING_CHUNK_SIZE = 80;

export const buildGroupingUserPrompt = (input: {
  restaurantName: string;
  category: string | null;
  variants: string[];
}): string => {
  const lines = [
    `식당명: "${input.restaurantName}"`,
    `카테고리: "${input.category ?? '미지정'}"`,
    `메뉴 변형들: ${JSON.stringify(input.variants, null, 0)}`,
  ];
  return lines.join('\n');
};
